package com.smalltalk.SmallTalkFootball.models.dto;

public class CardsItem{
	private String scoreInfoTime;
	private String homePlayerId;
	private String time;
	private String homeFault;
	private String awayPlayerId;
	private String card;
	private String awayFault;
	private String info;

	public String getScoreInfoTime(){
		return scoreInfoTime;
	}

	public String getHomePlayerId(){
		return homePlayerId;
	}

	public String getTime(){
		return time;
	}

	public String getHomeFault(){
		return homeFault;
	}

	public String getAwayPlayerId(){
		return awayPlayerId;
	}

	public String getCard(){
		return card;
	}

	public String getAwayFault(){
		return awayFault;
	}

	public String getInfo(){
		return info;
	}
}
