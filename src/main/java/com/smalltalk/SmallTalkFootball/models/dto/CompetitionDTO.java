package com.smalltalk.SmallTalkFootball.models.dto;

public class Competition{
	private String code;
	private String name;
	private int id;
	private String type;
	private String emblem;

	public String getCode(){
		return code;
	}

	public String getName(){
		return name;
	}

	public int getId(){
		return id;
	}

	public String getType(){
		return type;
	}

	public String getEmblem(){
		return emblem;
	}
}
