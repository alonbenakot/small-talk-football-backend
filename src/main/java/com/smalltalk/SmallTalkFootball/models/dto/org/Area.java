package com.smalltalk.SmallTalkFootball.models.dto;

public class Area{
	private String code;
	private String flag;
	private String name;
	private int id;

	public String getCode(){
		return code;
	}

	public String getFlag(){
		return flag;
	}

	public String getName(){
		return name;
	}

	public int getId(){
		return id;
	}
}
