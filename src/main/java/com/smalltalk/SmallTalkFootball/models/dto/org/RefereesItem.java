package com.smalltalk.SmallTalkFootball.models.dto;

public class RefereesItem{
	private String nationality;
	private String name;
	private int id;
	private String type;

	public String getNationality(){
		return nationality;
	}

	public String getName(){
		return name;
	}

	public int getId(){
		return id;
	}

	public String getType(){
		return type;
	}
}
