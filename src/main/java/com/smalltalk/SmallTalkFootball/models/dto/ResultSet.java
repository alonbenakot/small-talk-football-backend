public class ResultSet{
	private String last;
	private int count;
	private int played;
	private String first;

	public String getLast(){
		return last;
	}

	public int getCount(){
		return count;
	}

	public int getPlayed(){
		return played;
	}

	public String getFirst(){
		return first;
	}
}
